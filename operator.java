public class operator {
    public static void main(String[] args) {
        // Arithmatic operator
        int a = 10;
        int b = 10+a;
        System.out.println(b);
        
        // Modulo operator
        int d = 10;
        int e = 25%d;
        System.out.println(e);

        // Assignment operator
        int c= 5;
        c+= 6;
        System.out.println(c);

        System.out.println(6>10);  //comparison operator//
        System.out.println(64>9 && 78>13);  //Logical operator//
        System.out.println(7>9 || 9>0);
    }
}
